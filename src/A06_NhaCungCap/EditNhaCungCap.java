    private void luuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuButtonActionPerformed
        // TODO add your handling code here:
        if (maNCCTextField.getText().isEmpty()||tenNCCTextField.getText().isEmpty())    
        {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ Mã và Tên Nhà Cung Cấp");
        }
        else
        {
            int i=nhaCungCapService.getCountMANCC(maNCCTextField.getText());
            if (i!=0 &&!MANCCdangsua.equals(maNCCTextField.getText()))
            {
                 JOptionPane.showMessageDialog(this, "Mã Nhà Cung Cấp bị trùng lặp");
                 maNCCTextField.setText(MANCCdangsua);
            }
            else
            {
                nhaCungCap.setMANCC(maNCCTextField.getText());
                nhaCungCap.setTENNCC(tenNCCTextField.getText());
                nhaCungCap.setDIACHI(diaChiTextField.getText());
                nhaCungCap.setSDT(sdtTextField.getText());
                nhaCungCap.setGHICHU(ghiChuTextField.getText());      

                nhaCungCapService.updateNhaCungCap(nhaCungCap);
                JOptionPane.showMessageDialog(this, "Lưu thành công!");
            }
        }

    }//GEN-LAST:event_luuButtonActionPerformed

