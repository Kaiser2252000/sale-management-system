    private void xoaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaButtonActionPerformed
        // TODO add your handling code here:
        int row = nhaCungCapTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(FormNhaCungCap.this, "Vui lòng chọn Nhà Cung Cấp cần xóa","Lỗi", JOptionPane.ERROR_MESSAGE);
        }else{
            int confirm = JOptionPane.showConfirmDialog(FormNhaCungCap.this, "Bạn chắc chắn muốn xóa không?");
            
            if (confirm == JOptionPane.YES_OPTION) {
                String MANCC = String.valueOf( nhaCungCapTable.getValueAt(row, 0));
                
                nhaCungCapService.deleteNhaCungCap(MANCC);
                
                defaultTableModel.setRowCount(0);
                setTableData(nhaCungCapService.getAllNhaCungCaps());
            }
        }
    }//GEN-LAST:event_xoaButtonActionPerformed

    private void suaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaButtonActionPerformed
        // TODO add your handling code here:
        int row = nhaCungCapTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(FormNhaCungCap.this, "Vui lòng chọn Nhà Cung Cấp cần sửa","Lỗi", JOptionPane.ERROR_MESSAGE);
        }else{                      
            String MANCC = String.valueOf(nhaCungCapTable.getValueAt(row, 0));
                
            
            new EditNhaCungCap(MANCC).setVisible(true);
        }
    }//GEN-LAST:event_suaButtonActionPerformed

