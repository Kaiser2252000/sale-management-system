    private void luuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuButtonActionPerformed
        // TODO add your handling code here:
        if (maSPTextField.getText().isEmpty()||tenSPTextField.getText().isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ Mã và Tên Sản Phẩm");
        }
        else
        {
            int i= sanPhamService.getCountMASP(maSPTextField.getText());
            if (i!=0 && !MASPdangsua.equals(maSPTextField.getText()))
            {
                JOptionPane.showMessageDialog(this, "Mã Sản Phẩm bị trùng lặp");
                maSPTextField.setText(MASPdangsua);
            }
            else
            {
                if (!isNumeric(giaNhapTextField.getText().toString())||!isNumeric(giaBanTextField.getText().toString())||!isNumeric(soLuongTextField.getText().toString()))
                        
                {
                    JOptionPane.showMessageDialog(this, "Giá Bán, Giá Nhập, Số Lượng phải nhập định dạng số");
                }
                else
                {
                    sanPham.setMASP(maSPTextField.getText());
                    sanPham.setTENSP(tenSPTextField.getText());
                    Combobox combobox = (Combobox) maNCCComboBox.getSelectedItem();
                    sanPham.setMANCC(combobox.getId());
                    String loaiSP = String.valueOf(loaiSPComboBox.getSelectedItem());
                    sanPham.setLOAISP(loaiSP);     
                    sanPham.setGIANHAP(Double.valueOf(giaNhapTextField.getText()));
                    sanPham.setGIABAN(Double.valueOf(giaBanTextField.getText()));       
                    sanPham.setSOLUONG(Integer.valueOf(soLuongTextField.getText()));
                    sanPham.setGHICHU(ghiChuTextField.getText());

                    sanPhamService.updateSanPham(sanPham);
                    JOptionPane.showMessageDialog(this, "Lưu thành công!"); 
                }
            }
        }   
    }//GEN-LAST:event_luuButtonActionPerformed
    
    
