    public EditSanPham(String MASP) {
        sanPham = new SanPham();
        sanPhamService = new SanPhamService();
        sanPham = sanPhamService.getSanPhamByMASP(MASP);
        initComponents();
        try {
            Connection connection=JDBCConnection.getJDBCConnection();
            PreparedStatement preparedStatement = connection.prepareStatement("select * from nhacungcap");
            ResultSet resultSet = preparedStatement.executeQuery();
            while (resultSet.next())
            {
                Combobox combobox = new Combobox(resultSet.getString(1), resultSet.getString(2));
                maNCCComboBox.addItem(combobox);
            }
        } catch (Exception e) {
        }  
       
        for (int i=0;i<maNCCComboBox.getItemCount();i++)
        {
            Combobox combobox = (Combobox)maNCCComboBox.getItemAt(i);
            if (combobox.getId().equals(sanPham.getMANCC()))
            {    
                maNCCComboBox.setSelectedItem(combobox);
                break;
            }
            
        }
        MASPdangsua=sanPham.getMASP();
        maSPTextField.setText(sanPham.getMASP());
        tenSPTextField.setText(sanPham.getTENSP());
        //maNCCTextField.setText(sanPham.getMANCC());
        String loaiSP = sanPham.getLOAISP();
        loaiSPComboBox.setSelectedItem(loaiSP);
        giaNhapTextField.setText(sanPham.getGIANHAP().toString());
        giaBanTextField.setText(sanPham.getGIABAN().toString());
        soLuongTextField.setText(String.valueOf(sanPham.getSOLUONG()));
        ghiChuTextField.setText(sanPham.getGHICHU());
                
    }

