    private void xoaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaButtonActionPerformed
        // TODO add your handling code here:
        int row = sanPhamTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(FormSanPham.this, "Vui lòng chọn Sản Phẩm cần xóa","Lỗi", JOptionPane.ERROR_MESSAGE);
        }else{
            int confirm = JOptionPane.showConfirmDialog(FormSanPham.this, "Bạn chắc chắn muốn xóa không?");
            
            if (confirm == JOptionPane.YES_OPTION) {
                String MASP = String.valueOf(sanPhamTable.getValueAt(row, 0));
                
                sanPhamService.deleteSanPham(MASP);
                
                defaultTableModel.setRowCount(0);
                setTableData(sanPhamService.getAllHangHoas());
            }
        }
            
    }//GEN-LAST:event_xoaButtonActionPerformed

