    private void setTableData(List<SanPham> sanPhams){
        for(SanPham sanPham : sanPhams){
            defaultTableModel.addRow(new Object[]{sanPham.getMASP(), sanPham.getTENSP(),sanPham.getMANCC(), sanPham.getLOAISP(),
            sanPham.getGIANHAP(), sanPham.getGIABAN(), sanPham.getSOLUONG(), sanPham.getGHICHU()});                  
        }
    }
   
    private void Sapxep(String jQuery){
        try {           
            Connection Con = JDBCConnection.getJDBCConnection();
            PreparedStatement Pst = Con.prepareStatement(jQuery);
            ResultSet Rs = Pst.executeQuery();
            defaultTableModel.setRowCount(0);
            while (Rs.next()) {
                Object objList[] = {Rs.getString("MASP"), Rs.getString("TENSP"), Rs.getString("MANCC"), Rs.getString("LOAISP"), Rs.getDouble("GIANHAP"), Rs.getDouble("GIABAN"),Rs.getInt("SOLUONG"),Rs.getString("GHICHU")};
                defaultTableModel.addRow(objList);                
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormHoaDon.class.getName()).log(Level.SEVERE,null,ex);
        }
    }

