    private void timKiemTextFieldCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_timKiemTextFieldCaretUpdate
        // TODO add your handling code here:
        try {     
            if (loaiSPComboBox.getSelectedItem()=="Tất Cả") {
            Connection Con = JDBCConnection.getJDBCConnection();
            PreparedStatement Pst = Con.prepareStatement("select * from sanpham where MASP like N'%"+timKiemTextField.getText()+"%'or TENSP like N'%"+timKiemTextField.getText()+"%'");
            ResultSet Rs = Pst.executeQuery();
            defaultTableModel.setRowCount(0);
            while (Rs.next()) {
                Object objList[] = {Rs.getString("MASP"), Rs.getString("TENSP"), Rs.getString("MANCC"), Rs.getString("LOAISP"), Rs.getDouble("GIANHAP"), Rs.getDouble("GIABAN"),Rs.getInt("SOLUONG"),Rs.getString("GHICHU")};
                defaultTableModel.addRow(objList);                
            }
            }
            else
            {
                Connection Con = JDBCConnection.getJDBCConnection();
                PreparedStatement Pst = Con.prepareStatement("select * from sanpham where (MASP like N'%"+timKiemTextField.getText()+"%'or TENSP like N'%"+timKiemTextField.getText()+"%') and LOAISP like N'%"+loaiSPComboBox.getSelectedItem().toString()+"%'");
                ResultSet Rs = Pst.executeQuery();
                defaultTableModel.setRowCount(0);
                while (Rs.next()) {
                Object objList[] = {Rs.getString("MASP"), Rs.getString("TENSP"), Rs.getString("MANCC"), Rs.getString("LOAISP"), Rs.getDouble("GIANHAP"), Rs.getDouble("GIABAN"),Rs.getInt("SOLUONG"),Rs.getString("GHICHU")};
                defaultTableModel.addRow(objList);                
            }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormSanPham.class.getName()).log(Level.SEVERE,null,ex);
        }
    }//GEN-LAST:event_timKiemTextFieldCaretUpdate

    private void loaiSPComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_loaiSPComboBoxItemStateChanged
        // TODO add your handling code here:
        if (loaiSPComboBox.getSelectedItem()=="Tất Cả") {
                defaultTableModel.setRowCount(0);
                setTableData(sanPhamService.getAllHangHoas());
            }else{
            //select = LoaiHHCbx.getSelectedItem().toString();
            try {           
            Connection Con = JDBCConnection.getJDBCConnection();
            PreparedStatement Pst = Con.prepareStatement("select * from sanpham where (MASP like N'%"+timKiemTextField.getText()+"%'or TENSP like N'%"+timKiemTextField.getText()+"%') and LOAISP like N'%"+loaiSPComboBox.getSelectedItem().toString()+"%' ORDER BY SOLUONG DESC");
            ResultSet Rs = Pst.executeQuery();
            defaultTableModel = (DefaultTableModel)sanPhamTable.getModel();
            defaultTableModel.setRowCount(0);
            while (Rs.next()) {
                Object objList[] = {Rs.getString("MASP"), Rs.getString("TENSP"), Rs.getString("MANCC"), Rs.getString("LOAISP"), Rs.getDouble("GIANHAP"), Rs.getDouble("GIABAN"),Rs.getInt("SOLUONG"),Rs.getString("GHICHU")};
                defaultTableModel.addRow(objList);                
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormSanPham.class.getName()).log(Level.SEVERE,null,ex);
        }
        }
    }//GEN-LAST:event_loaiSPComboBoxItemStateChanged

